package JavaPractice;

public class JavaPractice {

	public static void main(String[] args) {
		
		int counter =1, num;
		for(int i=1; i<=5; i++)
		{
			System.out.println();
			num=1;
			for(int j=1; j<=counter; j++)
			{
				System.out.print(num);
				num++;
			}
			
			counter++;
		}

	}

}
